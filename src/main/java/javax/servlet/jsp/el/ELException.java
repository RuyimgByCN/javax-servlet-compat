package javax.servlet.jsp.el;

public class ELException extends jakarta.servlet.jsp.el.ELException {
    public ELException() { super(); }
    public ELException(String msg) { super(msg); }
    public ELException(String message, Throwable cause) { super(message, cause); }
    public ELException(Throwable cause) { super(cause); }
}
